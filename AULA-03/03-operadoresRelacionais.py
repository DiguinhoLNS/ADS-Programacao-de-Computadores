x = float(input("Digite o primeiro número: "))
y = float(input("Digite o segundo número: "))

print(f"{x} é maior que {y}? {x > y}")

print(f"{x} é maior ou igual a {y}? {x >= y}")

print(f"{x} é menor que {y}? {x < y}")

print(f"{x} é menor ou igual a {y}? {x <= y}")

print(f"{x} é diferente que {y}? {x != y}")

print(f"{x} é igual a {y}? {x == y}")